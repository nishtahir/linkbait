package com.nishtahir.linkbait.core.model

/**
 * Model for Plugin
 */
class Plugin {

    /**
     * Version information about plugin
     */
    String version

    /**
     * Title of the plugin
     */
    String title

    /**
     * Description of the plugin
     */
    String description

    /**
     * Author of the plugin
     */
    String author

    /**
     * Link to repository of the plugin
     */
    String url

    /**
     * Class name for handler to load
     */
    String handler

}
